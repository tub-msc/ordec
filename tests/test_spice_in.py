def test_build_schematic_standalone_auto_symbol():
    lib = ExtLibrary()
    lib.read_spice(stdcell_spice, device_map=ihp130.device_map)

    # Auto-generated symbol matches the subckt port list.
    sym = lib["sg13g2_inv_1"].symbol
    assert {p.full_path_str() for p in sym.all(Pin)} == {"A", "Y", "VDD", "VSS"}

    sch = lib["sg13g2_inv_1"].schematic
    insts = list(sch.all(SchemInstance))
    assert len(insts) == 2
    cells = {type(i.symbol.cell).__name__ for i in insts}
    assert cells == {"Nmos", "Pmos"}
    # Parameter mapping: l/w as R, ng/m as int; ad/as/pd/ps dropped.
    nmos = next(i.symbol.cell for i in insts if type(i.symbol.cell).__name__ == "Nmos")
    assert nmos.l == R("130n") and nmos.w == R("740n")
    assert nmos.ng == 1 and nmos.m == 1


def test_build_larger_cell():
    lib = ExtLibrary()
    lib.read_spice(stdcell_spice, device_map=ihp130.device_map)
    sch = lib["sg13g2_dfrbp_1"].schematic
    assert len(list(sch.all(SchemInstance))) == 34


def test_lef_symbol_wins_over_autogenerated():
    lib = ExtLibrary()
    lib.read_lef(stdcell_lef)
    lib.read_spice(stdcell_spice, device_map=ihp130.device_map)

    sym = lib["sg13g2_inv_1"].symbol
    # Pin directions come from LEF, not the Inout auto-symbol fallback.
    assert sym.A.pintype == PinType.In
    assert sym.Y.pintype == PinType.Out

    sch = lib["sg13g2_inv_1"].schematic
    assert sch.Y.pin.pintype == PinType.Out
    assert len(list(sch.all(SchemInstance))) == 2


def test_unknown_model_raises(tmp_path):
    netlist = tmp_path / "bad.spice"
    netlist.write_text(".subckt bad a b\nX0 a b mysterydev\n.ends\n")
    lib = ExtLibrary()
    lib.read_spice(netlist, device_map=ihp130.device_map)
    with pytest.raises(SpiceImportError, match="mysterydev"):
        lib["bad"].schematic


def test_duplicate_schematic_source_raises(tmp_path):
    from ordec.extlibrary import ExtLibraryError
    netlist = tmp_path / "dup.spice"
    netlist.write_text(".subckt dup a b\n.ends\n")
    lib = ExtLibrary()
    lib.read_spice(netlist, device_map=ihp130.device_map)
    with pytest.raises(ExtLibraryError, match="dup"):
        lib.read_spice(netlist, device_map=ihp130.device_map)
